<template>
  <el-row class="listOne">
    <el-row class="content_wB">
      <el-form
        :label-position="right"
        label-width="120px"
        :rules="rules"
        ref="formOne"
        :model="form"
      >
        <el-row>
          <el-row class="tit">
            <el-row class="name">
              招采信息
            </el-row>
            <el-row class="desc">
              <em>ISSUING
              </em>
              <span>
                PURCHASING
              </span>
              <span>
                INFORMATION
              </span>
            </el-row>
            <el-row class="line">
              <span />
            </el-row>
          </el-row>
          <el-form-item label="项目类别：" prop="class">
            <el-select v-model="form.class" placeholder="请选择项目类别">
              <el-option
                v-for="item in typeList"
                :key="item.value"
                :label="item.label"
                :value="item.value"
              />
            </el-select>
          </el-form-item>
          <el-form-item label="招采材料：" prop="material">
            <el-input v-model="form.material" placeholder="请输入招采材料" />
          </el-form-item>
          <el-form-item label="材料要求：">
            <el-input v-model="form.claim" placeholder="请输入材料要求" />
          </el-form-item>
          <el-row class="tips">
            请上传产品图片 <span>（图片限制在2M以内，最多上传3张，支持PNG\PG\JPEG\GIF格式）</span>
          </el-row>
          <el-row class="upImg">
            <el-upload
              accept="image/*"
              action="/api/file/dalaba/file/upload.json"
              list-type="picture-card"
              :on-success="(response, file, fileList)=>uploadPicture(response, file, fileList)"
              :on-remove="(file, fileList)=>handleRemove(file, fileList)"
              :limit="3"
              :before-upload="beforeAvatarUpload"
              :on-exceed="handleExceed"
            >
              <i class="el-icon-plus" />
            </el-upload>
          </el-row>
          <el-form-item label="工地地区：" prop="area">
            <el-cascader
              v-model="form.area"
              :options="areaList"
              :props="{ expandTrigger: 'hover' }"
              clearable
            />
          </el-form-item>
          <el-form-item label="详细地址：">
            <el-input v-model="form.address" placeholder="请输入详细地址" />
          </el-form-item>
          <el-form-item label="公司名称：" prop="companyName">
            <el-input v-model="form.companyName" placeholder="请输入公司名称" />
          </el-form-item>
          <el-form-item label="联系人姓名：" prop="nameS">
            <el-input v-model="form.nameS" placeholder="请输入联系人" />
          </el-form-item>
          <el-form-item label="联系人职位：">
            <el-input v-model="form.positionLevel" placeholder="请输入联系人职位" />
          </el-form-item>
          <el-form-item label="联系人电话：" prop="phone">
            <el-input v-model="form.phone" type="text" placeholder="请输入联系人电话" />
          </el-form-item>
          <div class="sub" @click="onSub">
            提交
          </div>
        </el-row>
      </el-form>
      <el-dialog
        title="提示"
        :visible.sync="dialogVisible"
        width="22%"
        :before-close="handleClose"
      >
        <span>{{ errorText }}</span>
        <span slot="footer" class="dialog-footer">
          <el-button @click="dialogVisible = false">取 消</el-button>
          <el-button type="primary" @click="dialogVisible = false">确 定</el-button>
        </span>
      </el-dialog>
    </el-row>
  </el-row>
</template>

<script>
import { HomeService } from '@/services/purchaseList'
export default {
  data () {
    const checkPhone = (rule, value, callback) => {
      if (!value) {
        return callback(new Error('号码不能为空'))
      }
      setTimeout(() => {
        const mobile = /^((13[0-9]{9})|(18[0-9]{9})|(14[0-9]{9})|(17[0-9]{9})|(15[0-9]{9})|(19[0-9]{9}))$/
        const tel = /^\d{3,4}-\d{7,9}$/
        if (!mobile.test(value) && !tel.test(value)) {
          return callback(new Error('请输入正确的电话'))
        } else {
          callback()
        }
      }, 1000)
    }
    return {
      dialogImageUrl: '',
      right: 'right',
      errorText: '', // 错误信息展示
      dialogVisible: false,
      form: {
        class: '', // 项目类别
        nameS: '', // 联系人姓名
        material: '', // 材料
        claim: '', // 材料要求
        pic: [], // 产品图片
        area: '', // 工地地区
        address: '', // 详细地址
        companyName: '', // 公司名称
        positionLevel: '', // 联系人职位
        phone: '' // 联系人电话
      },
      rules: {
        phone: [{ required: true, trigger: 'blur', validator: checkPhone }],
        nameS: [
          { required: true, message: '请输入联系人名称', trigger: 'blur' }
        ],
        class: [
          { required: true, message: '请选择项目类别', trigger: 'change' }
        ],
        material: [
          { required: true, message: '请输入招采材料', trigger: 'blur' }
        ],
        area: [
          { required: true, message: '请选择工地地区', trigger: 'change' }
        ]
      },
      areaList: [],
      typeList: []
    }
  },
  mounted () {
    this.getArea()
    this.getType()
  },
  methods: {
    onSub () {
      if (this.form.class === '') {
        this.errorText = '请选择项目类别！'
        this.dialogVisible = true
      } else if (this.form.material === '') {
        this.errorText = '请输入招采材料！'
        this.dialogVisible = true
      } else if (this.form.area === '') {
        this.errorText = '请选择工地地区！'
        this.dialogVisible = true
      } else if (this.form.nameS === '') {
        this.errorText = '请输入联系人姓名！'
        this.dialogVisible = true
      } else if (this.form.phone === '') {
        this.errorText = '请输入联系人电话！'
        this.dialogVisible = true
      } else {
        this.$refs.formOne.validate((valid) => {
          if (!valid) {
            this.$message({
              message: '你填写的信息不完整哦！~~~',
              type: 'warning'
            })
          } else {
            this.postRecruit()
          }
        })
      }
    },
    postRecruit () {
      const params = {
        address: this.form.address,
        area: this.form.area[2] || 0,
        city: this.form.area[1] || 0,
        companyName: this.form.companyName,
        contactName: this.form.nameS,
        contactPhone: this.form.phone,
        contactPosition: this.form.positionLevel,
        materials: this.form.material,
        projectCategoryId: this.form.class,
        province: this.form.area[0] || 0,
        purchaseAnnexeList: this.form.pic,
        requirement: this.form.claim,
        supplement: '',
        type: 1
      }
      const homeService = new HomeService({ $axios: this.$axios, app: { $cookies: this.$cookies } })
      homeService.postRecruit(params).then((res) => {
        console.log(res)
        if (res.status === 200) {
          this.$message({
            message: '上传成功！请等待审核！！！',
            type: 'success'
          })
          this.$store.commit('release/changeNumber', 0)
          this.$nextTick(() => {
            this.$nuxt.$loading.start()
            this.$store.commit('release/changeNumber', 1)
            setTimeout(() => {
              this.$nuxt.$loading.finish()
            }, 500)
          })
        }
      })
    },
    handleClose () {
      this.dialogVisible = false
    },
    // 图片移除
    handleRemove (file, fileList) {
      const that = this
      const fileName = file.response.uri
      that.delone(fileName)
    },

    // 删除图片数组具体函数del
    delone (fileName) {
      const that = this
      const arr = that.form.pic
      const del = arr.indexOf(fileName)
      that.form.pic.splice(del, 1)
      console.log(this.form.pic)
    },

    // 图片上传
    uploadPicture (response, file, fileList) {
      console.log(file, '杀杀杀')
      this.form.pic.push({ type: 1, 'linkUrl': file.response.uri })
      this.dialogImageUrl = file.url
      console.log(this.form.pic)
    },
    beforeAvatarUpload (file) { // 文件上传之前调用做一些拦截限制
      console.log(file)
      const isJPG = true
      // const isJPG = file.type === 'image/jpeg';
      const isLt2M = file.size / 1024 / 1024 < 2

      // if (!isJPG) {
      //   this.$message.error('上传头像图片只能是 JPG 格式!');
      // }
      if (!isLt2M) {
        this.$message.error('上传图片大小不能超过 2MB!')
      }
      return isJPG && isLt2M
    },
    handleExceed () { // 限制图片张数
      this.$message({
        message: '最多只能上传3张图片',
        type: 'error'
      })
    },
    getArea () { // 获取地区
      const homeService = new HomeService({ $axios: this.$axios, app: { $cookies: this.$cookies } })
      homeService.getArea().then((res) => {
        console.log(res.data)
        this.areaList = this.convertTree(res.data.results)
      })
    },
    getType () { // 获取类型
      const homeService = new HomeService({ $axios: this.$axios, app: { $cookies: this.$cookies } })
      homeService.getType().then((res) => {
        console.log(res.data)
        this.typeList = this.convertTree(res.data.results)
      })
    },
    convertTree (tree) {
      const result = []
      // eslint-disable-next-line no-unused-vars
      // 遍历 tree
      tree.forEach((item) => {
        // 解构赋值
        let {
          // eslint-disable-next-line prefer-const
          name: label,
          id: value,
          children
        } = item
        // 如果有子节点，递归
        if (value === 0) {
          value = 0 // 生成字母和数字的随机数
        }
        if (children) {
          children = this.convertTree(children)
        }
        result.push({
          value,
          label,
          children
        })
      })

      return result
    }
  }
}
</script>

